const numero1 = 30;
const numero2 = 10;
let resultado;

// suma
resultado = numero1 + numero2;
console.log(resultado);

// resta
resultado = numero1 - numero2;
console.log(resultado);

// multiplicacion
resultado = numero1 * numero2;
console.log(resultado);

// division
resultado = numero1 / numero2;
console.log(resultado);

// modulo
resultado = numero1 % numero2;
console.log(resultado);